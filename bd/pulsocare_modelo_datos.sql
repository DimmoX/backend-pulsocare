    FECHA_EVENTO    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_BITACORA      PRIMARY KEY (ID_BITACORA),
    CONSTRAINT FK_BITACORA_USR  FOREIGN KEY (ID_USUARIO)  REFERENCES PC_USUARIO (ID_USUARIO),
    CONSTRAINT FK_BITACORA_PAC  FOREIGN KEY (ID_PACIENTE) REFERENCES PC_PACIENTE (ID_PACIENTE)
);

--==============================================================================

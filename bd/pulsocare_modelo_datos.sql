------------------------------------------------------------------- ROL
CREATE TABLE PC_ROL (
    ID_ROL        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODIGO        VARCHAR2(20)  NOT NULL,
    NOMBRE        VARCHAR2(50)  NOT NULL,
    DESCRIPCION   VARCHAR2(200),
    CONSTRAINT PK_ROL        PRIMARY KEY (ID_ROL),
    CONSTRAINT UQ_ROL_CODIGO UNIQUE (CODIGO)
);

------------------------------------------------------------------- PERMISO
CREATE TABLE PC_PERMISO (
    ID_PERMISO    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODIGO        VARCHAR2(20)  NOT NULL,   -- VER / EDITAR / COMPLETO
    NOMBRE        VARCHAR2(50)  NOT NULL,
    DESCRIPCION   VARCHAR2(200),
    CONSTRAINT PK_PERMISO        PRIMARY KEY (ID_PERMISO),
    CONSTRAINT UQ_PERMISO_CODIGO UNIQUE (CODIGO)
);

------------------------------------------------------------------- PARENTESCO
CREATE TABLE PC_PARENTESCO (
    ID_PARENTESCO NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE        VARCHAR2(50)  NOT NULL,   -- Hijo/a, Conyuge, Hermano/a, etc.
    CONSTRAINT PK_PARENTESCO        PRIMARY KEY (ID_PARENTESCO),
    CONSTRAINT UQ_PARENTESCO_NOMBRE UNIQUE (NOMBRE)
);

------------------------------------------------------------------- COMUNA
CREATE TABLE PC_COMUNA (
    ID_COMUNA     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE        VARCHAR2(80)  NOT NULL,
    REGION        VARCHAR2(80),
    CONSTRAINT PK_COMUNA        PRIMARY KEY (ID_COMUNA),
    CONSTRAINT UQ_COMUNA_NOMBRE UNIQUE (NOMBRE)
);

------------------------------------------------------------------- MODALIDAD_ATENCION
CREATE TABLE PC_MODALIDAD_ATENCION (
    ID_MODALIDAD  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    NOMBRE        VARCHAR2(50)  NOT NULL,   -- Domicilio, Clinica, Hosp. Domiciliaria
    CONSTRAINT PK_MODALIDAD        PRIMARY KEY (ID_MODALIDAD),
    CONSTRAINT UQ_MODALIDAD_NOMBRE UNIQUE (NOMBRE)
);

------------------------------------------------------------------- DIAGNOSTICO
CREATE TABLE PC_DIAGNOSTICO (
    ID_DIAGNOSTICO NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODIGO_ICD     VARCHAR2(15),            -- referencia opcional CIE-10
    NOMBRE         VARCHAR2(200) NOT NULL,  -- ej: Insuficiencia cardiaca congestiva
    CONSTRAINT PK_DIAGNOSTICO PRIMARY KEY (ID_DIAGNOSTICO)
);

------------------------------------------------------------------- SIGNO_VITAL (tipo + unidad)
CREATE TABLE PC_SIGNO_VITAL (
    ID_SIGNO_VITAL  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODIGO          VARCHAR2(15)  NOT NULL, -- FC, SPO2, PAS, PAD, TEMP, FR
    NOMBRE          VARCHAR2(60)  NOT NULL, -- Frecuencia cardiaca, etc.
    UNIDAD          VARCHAR2(15)  NOT NULL, -- bpm, %, mmHg, C, rpm
    ITEMID_MIMIC    NUMBER,                 -- itemid de origen en MIMIC (d_items)
    RANGO_MIN       NUMBER(6,2),            -- limite normal inferior por defecto
    RANGO_MAX       NUMBER(6,2),            -- limite normal superior por defecto
    DECIMALES       NUMBER(1)     DEFAULT 0 NOT NULL,
    CONSTRAINT PK_SIGNO_VITAL        PRIMARY KEY (ID_SIGNO_VITAL),
    CONSTRAINT UQ_SIGNO_VITAL_CODIGO UNIQUE (CODIGO),
    CONSTRAINT CK_SIGNO_VITAL_RANGO  CHECK (RANGO_MIN IS NULL OR RANGO_MAX IS NULL OR RANGO_MIN <= RANGO_MAX)
);

------------------------------------------------------------------- ESTADO_PACIENTE

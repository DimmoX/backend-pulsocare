--==============================================================================
-- 2. SEGURIDAD Y USUARIOS (RBAC)
--==============================================================================

------------------------------------------------------------------- ROL_PERMISO (N:M)
CREATE TABLE PC_ROL_PERMISO (
    ID_ROL        NUMBER NOT NULL,
    ID_PERMISO    NUMBER NOT NULL,
    CONSTRAINT PK_ROL_PERMISO       PRIMARY KEY (ID_ROL, ID_PERMISO),
    CONSTRAINT FK_ROLPERM_ROL       FOREIGN KEY (ID_ROL)     REFERENCES PC_ROL (ID_ROL),
    CONSTRAINT FK_ROLPERM_PERMISO   FOREIGN KEY (ID_PERMISO) REFERENCES PC_PERMISO (ID_PERMISO)
);

------------------------------------------------------------------- USUARIO
-- Usuarios de la plataforma: medico, enfermero, familiar y administrador.
-- La autenticacion se federa con Azure Entra ID (IDaaS); se guarda el
-- identificador externo (ENTRA_OID). HASH_CONTRASENA queda como respaldo local
-- opcional (ej. contrasena temporal creada por el administrador).
CREATE TABLE PC_USUARIO (
    ID_USUARIO        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_ROL            NUMBER        NOT NULL,
    ID_PARENTESCO     NUMBER,                       -- solo aplica a familiares
    NOMBRE            VARCHAR2(60)  NOT NULL,
    APELLIDO_PATERNO  VARCHAR2(60)  NOT NULL,
    APELLIDO_MATERNO  VARCHAR2(60),
    FECHA_NACIMIENTO  DATE,
    CORREO            VARCHAR2(120) NOT NULL,        -- correo institucional @pulsocare.cl
    TELEFONO          VARCHAR2(20),                  -- destino de notificaciones SMS
    ENTRA_OID         VARCHAR2(100),                 -- Object ID en Azure Entra ID
    HASH_CONTRASENA   VARCHAR2(255),                 -- respaldo local / clave temporal
    ESTADO            VARCHAR2(15)  DEFAULT 'ACTIVO' NOT NULL,
    FECHA_CREACION    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    ID_CREADO_POR     NUMBER,                        -- administrador que lo creo
    CONSTRAINT PK_USUARIO        PRIMARY KEY (ID_USUARIO),
    CONSTRAINT UQ_USUARIO_CORREO UNIQUE (CORREO),
    CONSTRAINT CK_USUARIO_ESTADO CHECK (ESTADO IN ('ACTIVO','INACTIVO')),
    CONSTRAINT FK_USUARIO_ROL        FOREIGN KEY (ID_ROL)        REFERENCES PC_ROL (ID_ROL),
    CONSTRAINT FK_USUARIO_PARENTESCO FOREIGN KEY (ID_PARENTESCO) REFERENCES PC_PARENTESCO (ID_PARENTESCO),
    CONSTRAINT FK_USUARIO_CREADOPOR  FOREIGN KEY (ID_CREADO_POR) REFERENCES PC_USUARIO (ID_USUARIO)
);

------------------------------------------------------------------- TOKEN_RECUPERACION
-- Soporta la pantalla "Recupera tu contrasena".
CREATE TABLE PC_TOKEN_RECUPERACION (
    ID_TOKEN          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO        NUMBER        NOT NULL,
    TOKEN_HASH        VARCHAR2(255) NOT NULL,
    FECHA_CREACION    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_EXPIRACION  TIMESTAMP     NOT NULL,
    USADO             NUMBER(1)     DEFAULT 0 NOT NULL,
    CONSTRAINT PK_TOKEN_RECUPERACION PRIMARY KEY (ID_TOKEN),
    CONSTRAINT CK_TOKEN_USADO        CHECK (USADO IN (0,1)),
    CONSTRAINT FK_TOKEN_USUARIO      FOREIGN KEY (ID_USUARIO) REFERENCES PC_USUARIO (ID_USUARIO)
);

--==============================================================================
-- 3. PACIENTES Y CONTEXTO CLINICO
--==============================================================================

------------------------------------------------------------------- PACIENTE
-- SUBJECT_ID vincula al paciente con su origen en el dataset MIMIC-IV.
CREATE TABLE PC_PACIENTE (
    ID_PACIENTE        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    SUBJECT_ID         NUMBER        NOT NULL,        -- identificador MIMIC-IV
    NOMBRE             VARCHAR2(60)  NOT NULL,
    APELLIDO_PATERNO   VARCHAR2(60)  NOT NULL,
    APELLIDO_MATERNO   VARCHAR2(60),
    FECHA_NACIMIENTO   DATE,
    SEXO               CHAR(1),                        -- M / F / O
    ID_COMUNA          NUMBER,
    ID_MODALIDAD       NUMBER,
    ID_ESTADO_PACIENTE NUMBER,
    FECHA_ALTA_SISTEMA TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_PACIENTE        PRIMARY KEY (ID_PACIENTE),
    CONSTRAINT UQ_PACIENTE_SUBJECT UNIQUE (SUBJECT_ID),
    CONSTRAINT CK_PACIENTE_SEXO   CHECK (SEXO IN ('M','F','O')),
    CONSTRAINT FK_PACIENTE_COMUNA    FOREIGN KEY (ID_COMUNA)          REFERENCES PC_COMUNA (ID_COMUNA),
    CONSTRAINT FK_PACIENTE_MODALIDAD FOREIGN KEY (ID_MODALIDAD)       REFERENCES PC_MODALIDAD_ATENCION (ID_MODALIDAD),
    CONSTRAINT FK_PACIENTE_ESTADO    FOREIGN KEY (ID_ESTADO_PACIENTE) REFERENCES PC_ESTADO_PACIENTE (ID_ESTADO_PACIENTE)
);

------------------------------------------------------------------- PACIENTE_DIAGNOSTICO (N:M)
CREATE TABLE PC_PACIENTE_DIAGNOSTICO (
    ID_PACIENTE     NUMBER      NOT NULL,
    ID_DIAGNOSTICO  NUMBER      NOT NULL,
    ES_PRINCIPAL    NUMBER(1)   DEFAULT 0 NOT NULL,
    FECHA_REGISTRO  DATE        DEFAULT SYSDATE NOT NULL,
    CONSTRAINT PK_PAC_DIAG       PRIMARY KEY (ID_PACIENTE, ID_DIAGNOSTICO),
    CONSTRAINT CK_PAC_DIAG_PRINC CHECK (ES_PRINCIPAL IN (0,1)),
    CONSTRAINT FK_PACDIAG_PAC    FOREIGN KEY (ID_PACIENTE)    REFERENCES PC_PACIENTE (ID_PACIENTE) ON DELETE CASCADE,
    CONSTRAINT FK_PACDIAG_DIAG   FOREIGN KEY (ID_DIAGNOSTICO) REFERENCES PC_DIAGNOSTICO (ID_DIAGNOSTICO)
);

------------------------------------------------------------------- EPISODIO_CLINICO
-- Periodo de atencion/monitoreo del paciente (mapea admission/icustay de MIMIC).
CREATE TABLE PC_EPISODIO_CLINICO (
    ID_EPISODIO      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_PACIENTE      NUMBER       NOT NULL,
    HADM_ID          NUMBER,                      -- ingreso hospitalario MIMIC
    STAY_ID          NUMBER,                      -- estancia MIMIC
    ID_MODALIDAD     NUMBER,
    UNIDAD           VARCHAR2(80),                -- unidad/servicio de atencion
    FECHA_INGRESO    TIMESTAMP    NOT NULL,
    FECHA_EGRESO     TIMESTAMP,
    ACTIVO           NUMBER(1)    DEFAULT 1 NOT NULL,
    CONSTRAINT PK_EPISODIO       PRIMARY KEY (ID_EPISODIO),
    CONSTRAINT CK_EPISODIO_ACT   CHECK (ACTIVO IN (0,1)),
    CONSTRAINT CK_EPISODIO_FECHA CHECK (FECHA_EGRESO IS NULL OR FECHA_EGRESO >= FECHA_INGRESO),
    CONSTRAINT FK_EPISODIO_PAC       FOREIGN KEY (ID_PACIENTE)  REFERENCES PC_PACIENTE (ID_PACIENTE),
    CONSTRAINT FK_EPISODIO_MODALIDAD FOREIGN KEY (ID_MODALIDAD) REFERENCES PC_MODALIDAD_ATENCION (ID_MODALIDAD)
);

------------------------------------------------------------------- ASIGNACION_CUIDADO (N:M usuario-paciente)
-- Define el equipo de cuidado: que medico/enfermero/familiar accede a que
-- paciente. Es la base del control de acceso por paciente.
CREATE TABLE PC_ASIGNACION_CUIDADO (
    ID_ASIGNACION   NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO      NUMBER      NOT NULL,
    ID_PACIENTE     NUMBER      NOT NULL,
    FECHA_INICIO    DATE        DEFAULT SYSDATE NOT NULL,
    FECHA_FIN       DATE,
    ACTIVO          NUMBER(1)   DEFAULT 1 NOT NULL,
    CONSTRAINT PK_ASIGNACION      PRIMARY KEY (ID_ASIGNACION),
    CONSTRAINT UQ_ASIGNACION      UNIQUE (ID_USUARIO, ID_PACIENTE, FECHA_INICIO),
    CONSTRAINT CK_ASIGNACION_ACT  CHECK (ACTIVO IN (0,1)),
    CONSTRAINT FK_ASIGN_USUARIO   FOREIGN KEY (ID_USUARIO)  REFERENCES PC_USUARIO (ID_USUARIO),
    CONSTRAINT FK_ASIGN_PACIENTE  FOREIGN KEY (ID_PACIENTE) REFERENCES PC_PACIENTE (ID_PACIENTE)
);

--==============================================================================
-- 4. UMBRALES, LECTURAS, ALERTAS Y NOTIFICACIONES (nucleo en tiempo real)
--==============================================================================

------------------------------------------------------------------- UMBRAL
-- Limites personalizados por paciente y signo vital. Es el dato que la
-- aplicacion cachea en ElastiCache para evaluar cada lectura en tiempo real.
CREATE TABLE PC_UMBRAL (
    ID_UMBRAL          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_PACIENTE        NUMBER       NOT NULL,
    ID_SIGNO_VITAL     NUMBER       NOT NULL,
    VALOR_MIN          NUMBER(6,2),              -- limite normal inferior
    VALOR_MAX          NUMBER(6,2),              -- limite normal superior
    VALOR_MIN_CRITICO  NUMBER(6,2),              -- umbral de alerta roja inferior
    VALOR_MAX_CRITICO  NUMBER(6,2),              -- umbral de alerta roja superior
    VIGENTE            NUMBER(1)    DEFAULT 1 NOT NULL,
    VIGENTE_DESDE      TIMESTAMP    DEFAULT SYSTIMESTAMP NOT NULL,
    ID_DEFINIDO_POR    NUMBER,                   -- medico que ajusto el umbral
    CONSTRAINT PK_UMBRAL          PRIMARY KEY (ID_UMBRAL),
    CONSTRAINT CK_UMBRAL_VIGENTE  CHECK (VIGENTE IN (0,1)),
    CONSTRAINT CK_UMBRAL_RANGO    CHECK (VALOR_MIN IS NULL OR VALOR_MAX IS NULL OR VALOR_MIN <= VALOR_MAX),
    CONSTRAINT FK_UMBRAL_PAC      FOREIGN KEY (ID_PACIENTE)     REFERENCES PC_PACIENTE (ID_PACIENTE),
    CONSTRAINT FK_UMBRAL_SIGNO    FOREIGN KEY (ID_SIGNO_VITAL)  REFERENCES PC_SIGNO_VITAL (ID_SIGNO_VITAL),
    CONSTRAINT FK_UMBRAL_DEFPOR   FOREIGN KEY (ID_DEFINIDO_POR) REFERENCES PC_USUARIO (ID_USUARIO)
);

------------------------------------------------------------------- LECTURA_SIGNO_VITAL (serie de tiempo)
-- Una fila por medicion. Es la tabla de mayor volumen del sistema.
-- SUGERENCIA (no obligatoria): en produccion conviene particionar por RANGE
-- mensual sobre FECHA_MEDICION para gestionar el historico. Se deja como tabla
-- estandar con indice compuesto para que el script sea directamente ejecutable.
CREATE TABLE PC_LECTURA_SIGNO_VITAL (
    ID_LECTURA        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_PACIENTE       NUMBER        NOT NULL,
    ID_EPISODIO       NUMBER,
    ID_SIGNO_VITAL    NUMBER        NOT NULL,
    VALOR_NUM         NUMBER(8,2),                 -- valor numerico medido
    VALOR_TEXTO       VARCHAR2(60),                -- valores no numericos (opcional)
    UNIDAD            VARCHAR2(15),                -- unidad al momento de medir
    FECHA_MEDICION    TIMESTAMP     NOT NULL,      -- marca de tiempo de la medicion
    FECHA_REGISTRO    TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL, -- ingesta en plataforma
    ID_ESTADO_LECTURA NUMBER,
    ORIGEN            VARCHAR2(30)  DEFAULT 'MIMIC_SIM' NOT NULL,
    CONSTRAINT PK_LECTURA       PRIMARY KEY (ID_LECTURA),
    CONSTRAINT FK_LECTURA_PAC      FOREIGN KEY (ID_PACIENTE)       REFERENCES PC_PACIENTE (ID_PACIENTE),
    CONSTRAINT FK_LECTURA_EPISODIO FOREIGN KEY (ID_EPISODIO)       REFERENCES PC_EPISODIO_CLINICO (ID_EPISODIO),
    CONSTRAINT FK_LECTURA_SIGNO    FOREIGN KEY (ID_SIGNO_VITAL)    REFERENCES PC_SIGNO_VITAL (ID_SIGNO_VITAL),
    CONSTRAINT FK_LECTURA_ESTADO   FOREIGN KEY (ID_ESTADO_LECTURA) REFERENCES PC_ESTADO_LECTURA (ID_ESTADO_LECTURA)
);

------------------------------------------------------------------- ALERTA
-- Evento generado cuando una lectura supera los umbrales del paciente.
CREATE TABLE PC_ALERTA (
    ID_ALERTA         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_LECTURA        NUMBER        NOT NULL,      -- lectura que la disparo
    ID_PACIENTE       NUMBER        NOT NULL,
    ID_SIGNO_VITAL    NUMBER        NOT NULL,
    ID_NIVEL_ALERTA   NUMBER        NOT NULL,      -- amarillo / rojo
    ID_ESTADO_ALERTA  NUMBER        NOT NULL,      -- generada / notificada / etc.
    VALOR_REGISTRADO  NUMBER(8,2),
    UMBRAL_VIOLADO    VARCHAR2(10),                -- MIN / MAX
    FECHA_GENERACION  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    ID_RECONOCIDA_POR NUMBER,                      -- usuario que reconoce la alerta
    FECHA_RECONOCIM   TIMESTAMP,
    CONSTRAINT PK_ALERTA        PRIMARY KEY (ID_ALERTA),
    CONSTRAINT CK_ALERTA_UMBRAL CHECK (UMBRAL_VIOLADO IS NULL OR UMBRAL_VIOLADO IN ('MIN','MAX')),
    CONSTRAINT FK_ALERTA_LECTURA  FOREIGN KEY (ID_LECTURA)        REFERENCES PC_LECTURA_SIGNO_VITAL (ID_LECTURA),
    CONSTRAINT FK_ALERTA_PAC      FOREIGN KEY (ID_PACIENTE)       REFERENCES PC_PACIENTE (ID_PACIENTE),
    CONSTRAINT FK_ALERTA_SIGNO    FOREIGN KEY (ID_SIGNO_VITAL)    REFERENCES PC_SIGNO_VITAL (ID_SIGNO_VITAL),
    CONSTRAINT FK_ALERTA_NIVEL    FOREIGN KEY (ID_NIVEL_ALERTA)   REFERENCES PC_NIVEL_ALERTA (ID_NIVEL_ALERTA),
    CONSTRAINT FK_ALERTA_ESTADO   FOREIGN KEY (ID_ESTADO_ALERTA)  REFERENCES PC_ESTADO_ALERTA (ID_ESTADO_ALERTA),
    CONSTRAINT FK_ALERTA_RECONPOR FOREIGN KEY (ID_RECONOCIDA_POR) REFERENCES PC_USUARIO (ID_USUARIO)
);

------------------------------------------------------------------- NOTIFICACION (SMS)
-- Registro del envio de SMS (AWS End User Messaging) a un destinatario.
CREATE TABLE PC_NOTIFICACION (
    ID_NOTIFICACION        NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_ALERTA              NUMBER        NOT NULL,
    ID_USUARIO_DESTINO     NUMBER        NOT NULL,
    ID_ESTADO_NOTIFICACION NUMBER        NOT NULL,
    CANAL                  VARCHAR2(15)  DEFAULT 'SMS' NOT NULL,
    TELEFONO_DESTINO       VARCHAR2(20),
    MENSAJE                VARCHAR2(480),
    ID_PROVEEDOR_MSG       VARCHAR2(100),          -- message id del proveedor
    INTENTOS               NUMBER(2)     DEFAULT 0 NOT NULL,
    FECHA_ENVIO            TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_ENTREGA          TIMESTAMP,
    CONSTRAINT PK_NOTIFICACION      PRIMARY KEY (ID_NOTIFICACION),
    CONSTRAINT FK_NOTIF_ALERTA      FOREIGN KEY (ID_ALERTA)              REFERENCES PC_ALERTA (ID_ALERTA),
    CONSTRAINT FK_NOTIF_USUARIO     FOREIGN KEY (ID_USUARIO_DESTINO)     REFERENCES PC_USUARIO (ID_USUARIO),
    CONSTRAINT FK_NOTIF_ESTADO      FOREIGN KEY (ID_ESTADO_NOTIFICACION) REFERENCES PC_ESTADO_NOTIFICACION (ID_ESTADO_NOTIFICACION)
);

--==============================================================================
-- 5. AUDITORIA (trazabilidad de accesos a datos clinicos)

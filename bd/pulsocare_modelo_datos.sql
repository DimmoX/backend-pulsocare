------------------------------------------------------------------- BITACORA_ACCESO
CREATE TABLE PC_BITACORA_ACCESO (
    ID_BITACORA     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ID_USUARIO      NUMBER        NOT NULL,
    ID_PACIENTE     NUMBER,
    ACCION          VARCHAR2(50)  NOT NULL,        -- LOGIN, VER_PACIENTE, EDITAR_UMBRAL...
    DETALLE         VARCHAR2(400),
